
/* lab animation*/
TweenMax.to("#labrador_x5F_retriever", 0.001, {x:580});
TweenMax.to("#lab_x5F_text", 0.001, {opacity:0});
function moveDog(){
TweenMax.to("#labrador_x5F_retriever", 1, {x:0});
TweenMax.to("#lab_x5F_text", 1, {opacity:1,delay:1});
}
document.querySelector("#labrador_x5F_retriever").addEventListener("mouseover", moveDog)

